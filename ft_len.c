#include "libft.h"

size_t	ft_len(char const *str, char c)
{
	int		i;
	int		wl;

	i = 0;
	wl = 0;
	while (str[i] == c)
		i++;
	while (str[i] != c && str[i] != '\0')
	{
		wl++;
		i++;
	}
	return (wl);
}
